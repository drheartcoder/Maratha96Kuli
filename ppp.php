<div id="title-blac3">&nbsp;&nbsp;About</div>
<p>In 2008 Ulli Hammann and Hagen Pfennigstorf aka Autodeep stepped up with their very own electronica label called Love Hertz in early 2008. The label's distinctive, yet genre-clashing, deep Tech House sound soon established the imprint as a recognizable force for quality club sound, gaining support from international top players like Luciano, SIS, Timo Maas, Paco Osuna, Len Faki, Len Faki, Sante, Gregor Tresher, Robert Dietz, Someone Else, Ilario Alicante, Lee Van Dowski and many more.</p>
<p>A strong in-house roster of upcoming talents combined with remixes by prolific producers like Kruse &amp; Nurnberg, Deepchild, Microdinamic, Stipe, Beaner and lots more the present as well as the future look bright for the Hannover based venture.<br />
  Love Hertz' approach reflects the current underground club sound as well as a deep respect for the pioneers of electronic music from Detroit, Chicago and the UK. With its steady output of high quality releases the label is considered a tried and tested outlet for quality underground dance music ranging from deep house to minimalistic tech sounds.</p>
<p>So better be prepared for versatile and forward-thinking releases from a label that obviously doesn't care about contemporary stereotypes and musical limitations- it's all about the love for electronic music and sweaty clubs!</p>
